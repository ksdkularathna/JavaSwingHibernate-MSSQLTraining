package com.swing.training.dtos;

public class DrugDosageDto {

	private int drugId;
	private String name;
	private String dosage;

	public DrugDosageDto() {
	}

	public DrugDosageDto(int id, String name, String dosage) {

		this.drugId = id;
		this.name = name;
		this.dosage = dosage;
	}

	public int getId() {
		return drugId;
	}

	public void setDrugId(int id) {
		this.drugId = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDosage() {
		return dosage;
	}

	public void setDosage(String dosage) {
		this.dosage = dosage;
	}
}
